package java_cwh.basic;

public class Class3 {
    public static void main(String[] args) {

        byte byteAge = 34;
        System.out.println(byteAge);

        int intAge = 56;
        System.out.println(intAge);

        short shortAge = 87;
        System.out.println(shortAge);

        long longAge = 5666666666666L;
        System.out.println(longAge);

        float floatAge = 5.6f;
        System.out.println(floatAge);

        double doubleAge = 4.66;
        System.out.println(doubleAge);

        char charA = 'A';
        System.out.println(charA);

        boolean booleanA = true;
        System.out.println(booleanA);

        String stringStr = "Harry";
        System.out.println(stringStr);
    }
}
